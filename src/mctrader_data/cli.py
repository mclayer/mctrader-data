"""mctrader-data CLI — backfill orchestration (one-shot, MCT-15 first commit)."""

from __future__ import annotations

import sys
from datetime import datetime, timedelta, timezone
from pathlib import Path

import click

from mctrader_market.types import Symbol, Timeframe

from mctrader_data.path import derive_partition_path, resolve_data_root
from mctrader_data.policy import PartialFailurePolicy


def _parse_iso_utc(value: str) -> datetime:
    if value.endswith("Z"):
        value = value[:-1] + "+00:00"
    dt = datetime.fromisoformat(value)
    if dt.tzinfo is None:
        raise click.BadParameter(f"naive datetime not allowed: {value!r}")
    if dt.utcoffset() != timedelta(0):
        raise click.BadParameter(f"non-UTC offset not allowed: {value!r}")
    return dt.replace(tzinfo=timezone.utc)


def _last_closed_boundary(now_utc: datetime, timeframe: Timeframe) -> datetime:
    """Return the most recent closed-bar boundary for the timeframe.

    1h: floor to UTC hour. 1d: KST midnight (UTC offset +9).
    """
    if timeframe is Timeframe.D1:
        kst = timezone(timedelta(hours=9))
        kst_now = now_utc.astimezone(kst)
        midnight_kst = kst_now.replace(hour=0, minute=0, second=0, microsecond=0)
        return midnight_kst.astimezone(timezone.utc)
    delta = timeframe.delta
    seconds = int(delta.total_seconds())
    epoch = int(now_utc.timestamp())
    floor_epoch = epoch - (epoch % seconds)
    return datetime.fromtimestamp(floor_epoch, tz=timezone.utc)


@click.group()
def main() -> None:
    """mctrader-data CLI."""


@main.command()
@click.option("--exchange", required=True, help='e.g. "bithumb"')
@click.option("--symbol", required=True, help='canonical "{quote}-{base}" e.g. "KRW-BTC"')
@click.option("--tf", "timeframe", required=True, type=click.Choice([t.value for t in Timeframe]))
@click.option("--days", type=int, default=None, help="N days back from --end (mutually exclusive with --start)")
@click.option("--start", "start_iso", type=str, default=None, help="ISO 8601 UTC inclusive")
@click.option("--end", "end_iso", type=str, default=None, help="ISO 8601 UTC exclusive (default = last closed bar)")
@click.option(
    "--policy",
    type=click.Choice([p.value for p in PartialFailurePolicy]),
    default=PartialFailurePolicy.HALT.value,
)
@click.option("--dry-run", is_flag=True, help="Print plan without fetching")
@click.option("--root", type=click.Path(path_type=Path), default=None)
@click.option("--snapshot-id", default=None, help="snapshot_id override (default = autogenerated)")
def backfill(
    exchange: str,
    symbol: str,
    timeframe: str,
    days: int | None,
    start_iso: str | None,
    end_iso: str | None,
    policy: str,
    dry_run: bool,
    root: Path | None,
    snapshot_id: str | None,
) -> None:
    """Backfill OHLCV candles for ``[start, end)`` half-open interval."""
    if days is not None and start_iso is not None:
        raise click.UsageError("--days and --start are mutually exclusive")

    sym = Symbol.from_string(symbol)
    tf = Timeframe(timeframe)

    end_utc = _parse_iso_utc(end_iso) if end_iso else _last_closed_boundary(datetime.now(timezone.utc), tf)
    if days is not None:
        if days <= 0:
            raise click.BadParameter("--days must be positive")
        start_utc = end_utc - timedelta(days=days)
    elif start_iso is not None:
        start_utc = _parse_iso_utc(start_iso)
    else:
        raise click.UsageError("must specify either --days or --start")

    if start_utc >= end_utc:
        raise click.BadParameter("start must be < end")

    root_resolved = resolve_data_root(root_override=root)
    partition_preview = derive_partition_path(
        root=root_resolved,
        exchange=exchange,
        symbol=sym,
        timeframe=tf,
        ts_utc=start_utc,
    )

    plan = {
        "exchange": exchange,
        "symbol": str(sym),
        "timeframe": tf.value,
        "start": start_utc.isoformat().replace("+00:00", "Z"),
        "end": end_utc.isoformat().replace("+00:00", "Z"),
        "policy": policy,
        "root": str(root_resolved),
        "snapshot_id": snapshot_id or "<auto>",
        "first_partition": str(partition_preview),
    }

    if dry_run:
        click.echo("[dry-run] Plan:")
        for key, value in plan.items():
            click.echo(f"  {key}: {value}")
        click.echo("[dry-run] No fetch performed.")
        return

    # MCT-12 retroactive sealing — wire BithumbCandleProvider → write_candles + lineage
    # (was previously stub printing "First-commit storage scaffold only").
    if exchange != "bithumb":
        raise click.UsageError(f"only 'bithumb' exchange is supported in v1, got {exchange!r}")

    import hashlib
    from datetime import datetime as _dt
    from datetime import timezone as _tz

    from mctrader_market_bithumb.adapter import BithumbCandleProvider

    from mctrader_data.lineage import write_lineage
    from mctrader_data.storage import write_candles

    click.echo("[backfill] fetching from Bithumb public REST...")
    provider = BithumbCandleProvider()
    candles = provider.get_candles(symbol=sym, timeframe=tf, start=start_utc, end=end_utc)
    if not candles:
        raise click.ClickException(
            f"no candles fetched for [{start_utc.isoformat()}, {end_utc.isoformat()})"
        )

    resolved_snapshot = snapshot_id or hashlib.sha256(
        f"{exchange}|{sym}|{tf.value}|{start_utc.isoformat()}|{end_utc.isoformat()}".encode()
    ).hexdigest()[:16]

    partition = write_candles(candles=candles, root=root_resolved, snapshot_id=resolved_snapshot)
    parquet_path = partition / f"part-{resolved_snapshot}.parquet"

    request_hash = hashlib.sha256(
        f"{sym}|{tf.value}|{start_utc.isoformat()}|{end_utc.isoformat()}".encode()
    ).hexdigest()
    response_hash = hashlib.sha256(
        "|".join(
            f"{c.ts_utc.isoformat()},{c.open},{c.high},{c.low},{c.close},{c.volume}"
            for c in candles
        ).encode()
    ).hexdigest()

    write_lineage(
        partition_dir=partition,
        snapshot_id=resolved_snapshot,
        exchange=exchange,
        endpoint="https://api.bithumb.com",
        request_params_hash=request_hash,
        fetched_at_utc=_dt.now(_tz.utc),
        response_hash=response_hash,
        adapter_name="mctrader-market-bithumb",
        adapter_version="0.3.0",
    )

    click.echo(f"[backfill] fetched {len(candles)} candles")
    click.echo(f"[backfill] window: {start_utc.isoformat()} → {end_utc.isoformat()}")
    click.echo(f"[backfill] partition: {partition}")
    click.echo(f"[backfill] parquet: {parquet_path}")
    click.echo(f"[backfill] snapshot_id: {resolved_snapshot}")
    sys.exit(0)


@main.command()
@click.option(
    "--symbols", default=None,
    help='Explicit symbols comma-separated, e.g. "KRW-BTC,KRW-ETH". Mutex with --top-n.',
)
@click.option(
    "--top-n", type=int, default=None,
    help="Query Bithumb ticker at startup, pick top N by 24h volume. Mutex with --symbols.",
)
@click.option(
    "--include", default="transactions,orderbook,orderbook_snapshot",
    help='Channels comma-separated: "transactions" + "orderbook" + "orderbook_snapshot" (default all three).',
)
@click.option("--exchange", default="bithumb", help='Only "bithumb" supported in v1.')
@click.option(
    "--root", type=click.Path(path_type=Path), default=None,
    help="Storage root. Default: $MCTRADER_DATA_ROOT or ~/.local/share/mctrader/data.",
)
@click.option(
    "--node-id", default=None,
    help="Node identifier for HA active-active (default: socket.gethostname()). "
         "Per MCT-91 — drives node= partition split + heartbeat-{node_id}.json.",
)
@click.option(
    "--heartbeat-interval", default=5.0, type=float,
    help="Heartbeat write interval in seconds (default 5.0).",
)
@click.option(
    "--heartbeat-root", type=click.Path(path_type=Path), default=None,
    help="Heartbeat artifact root (default: same as --root).",
)
@click.option(
    "--health-port", type=int, default=None,
    help="HTTP /health endpoint port (Docker HEALTHCHECK). "
         "Default: $MCTRADER_HEALTH_PORT or 8080. CFP-128 / ADR-033.",
)
@click.option("--log-level", default="INFO", type=click.Choice(["DEBUG", "INFO", "WARNING", "ERROR"]))
def collect(
    symbols: str | None,
    top_n: int | None,
    include: str,
    exchange: str,
    root: Path | None,
    node_id: str | None,
    heartbeat_interval: float,
    heartbeat_root: Path | None,
    health_port: int | None,
    log_level: str,
) -> None:
    """Forward-only WebSocket collector daemon (MCT-58).

    Subscribes to Bithumb public WebSocket for N symbols and writes Parquet
    append-only partitions under ``<root>/market/{ticks,orderbook}/...``.

    Designed for 24/7 systemd operation. SIGTERM = graceful drain + close.
    """
    import asyncio
    import logging

    from mctrader_data.collector import (
        CollectorDaemon,
        MultiSymbolCollector,
        fetch_top_n_krw_symbols,
    )

    logging.basicConfig(
        level=getattr(logging, log_level),
        format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
    )
    log = logging.getLogger("mctrader-data.collect")

    if symbols and top_n:
        raise click.UsageError("--symbols and --top-n are mutually exclusive")
    if not symbols and not top_n:
        raise click.UsageError("either --symbols or --top-n must be provided")

    chans = {c.strip() for c in include.split(",") if c.strip()}
    invalid = chans - {"transactions", "orderbook", "orderbook_snapshot"}
    if invalid:
        raise click.BadParameter(f"unknown channel(s): {invalid}")
    include_tx = "transactions" in chans
    include_ob = "orderbook" in chans
    include_ob_snapshot = "orderbook_snapshot" in chans

    root_resolved = resolve_data_root(root_override=root)
    log.info("storage root: %s", root_resolved)

    # MCT-91 — HA active-active resolution
    import socket as _socket
    resolved_node_id = node_id if node_id is not None else _socket.gethostname()
    resolved_heartbeat_root = heartbeat_root if heartbeat_root is not None else root_resolved
    log.info(
        "HA: node_id=%s heartbeat_root=%s heartbeat_interval=%.1fs",
        resolved_node_id, resolved_heartbeat_root, heartbeat_interval,
    )

    async def _amain() -> None:
        import asyncio as _asyncio
        from datetime import datetime, timezone

        from mctrader_data.heartbeat import HeartbeatWriter
        from mctrader_data.manifest import CollectorManifest, derive_collector_run_id

        if symbols:
            sym_list = [Symbol.from_string(s.strip()) for s in symbols.split(",") if s.strip()]
            selection_method = "explicit"
        else:
            assert top_n is not None  # guarded above by mutex check
            log.info("querying Bithumb ticker for top %d KRW pairs by 24h volume...", top_n)
            sym_list = await fetch_top_n_krw_symbols(n=top_n)
            log.info("selected: %s", [str(s) for s in sym_list])
            selection_method = "top_n_volume"

        started_at = datetime.now(timezone.utc)
        # MCT-91 — HA collector_run_id = {node_id}-{UTC_compact_ts} (legacy hash if node_id=None)
        run_id = derive_collector_run_id(
            started_at_utc=started_at,
            exchange=exchange,
            selected_symbols=[str(s) for s in sym_list],
            node_id=resolved_node_id,
        )
        channels: list[str] = []
        if include_tx:
            channels.append("transaction")
        if include_ob:
            channels.append("orderbookdepth")
        if include_ob_snapshot:
            channels.append("orderbooksnapshot")
        manifest = CollectorManifest(
            collector_run_id=run_id,
            started_at_utc=started_at,
            exchange=exchange,
            selected_symbols=[str(s) for s in sym_list],
            channels=channels,
            selection_method=selection_method,  # type: ignore[arg-type]
            top_n=top_n,
            node_id=resolved_node_id,
        )

        # MCT-91 — heartbeat writer (HA active-active)
        heartbeat = HeartbeatWriter(
            root=resolved_heartbeat_root,
            node_id=resolved_node_id,
            interval_seconds=heartbeat_interval,
        )

        # CFP-128 / ADR-033 Pilot — HealthServer for Docker HEALTHCHECK
        from mctrader_data.health_server import HealthServer
        health = HealthServer(heartbeat_writer=heartbeat, port=health_port)
        log.info("health server bound port=%d", health.port)

        daemons = [
            CollectorDaemon(
                root=root_resolved, exchange=exchange, symbol=sym,
                include_transactions=include_tx, include_orderbook=include_ob,
                include_orderbook_snapshot=include_ob_snapshot,
                snapshot_id=run_id,
                node_id=resolved_node_id, collector_run_id=run_id,
                heartbeat_writer=heartbeat,
            )
            for sym in sym_list
        ]
        collector = MultiSymbolCollector(
            daemons, manifest=manifest, manifest_root=root_resolved,
            heartbeat_writer=heartbeat,
            health_server=health,
        )

        # MCT-104 — MetadataRefreshScheduler (daily §D13 refresh, separate task)
        if include_ob_snapshot:
            from mctrader_data.collector import MetadataRefreshScheduler
            import contextlib as _ctx
            metadata_scheduler = MetadataRefreshScheduler(
                root=root_resolved,
                exchange=exchange,
                node_id=resolved_node_id,
                collector_run_id=run_id,
            )
            metadata_task = _asyncio.create_task(metadata_scheduler.run())
            log.info("[metadata] refresh scheduler started")
        else:
            metadata_task = None

        try:
            await collector.run()
        finally:
            if metadata_task is not None:
                metadata_task.cancel()
                with _ctx.suppress(_asyncio.CancelledError, Exception):
                    await metadata_task
                log.info("[metadata] refresh scheduler shutdown")

    try:
        asyncio.run(_amain())
    except KeyboardInterrupt:
        log.info("KeyboardInterrupt — shutting down")
        sys.exit(0)


# MCT-93 (X4 of MCT-89) — diagnostic surface


def _level_color(level: int) -> str:
    if level == 0:
        return "\033[32m"  # green
    if level == 1:
        return "\033[33m"  # yellow
    return "\033[31m"  # red


@main.command()
@click.option("--root", type=click.Path(path_type=Path), default=None)
@click.option("--fresh-yellow-seconds", default=10.0, type=float)
@click.option("--fresh-red-seconds", default=30.0, type=float)
@click.option("--lag-yellow-seconds", default=60.0, type=float)
@click.option("--lag-red-seconds", default=300.0, type=float)
@click.option("--format", "fmt", type=click.Choice(["human", "json"]), default="human")
@click.option("--no-color", is_flag=True, help="Disable ANSI color codes (auto-disabled in JSON).")
def status(
    root: Path | None,
    fresh_yellow_seconds: float,
    fresh_red_seconds: float,
    lag_yellow_seconds: float,
    lag_red_seconds: float,
    fmt: str,
    no_color: bool,
) -> None:
    """Show heartbeat freshness / lag / dedup metrics for all nodes.

    Exit code: 0 (all green) / 1 (any yellow) / 2 (any red or no heartbeat).
    """
    import json as _json
    from datetime import datetime as _datetime
    from datetime import timezone as _tz

    root_resolved = resolve_data_root(root_override=root)
    hb_dir = root_resolved / "market" / "manifest"

    files = sorted(hb_dir.glob("heartbeat-*.json"))
    if not files:
        click.echo(f"no heartbeat files in {hb_dir}", err=True)
        sys.exit(2)

    now_wall = _datetime.now(_tz.utc)
    nodes: list[dict] = []
    worst_level = 0

    for f in files:
        node_id = f.stem.removeprefix("heartbeat-")
        try:
            data = _json.loads(f.read_text(encoding="utf-8"))
        except (OSError, _json.JSONDecodeError) as e:
            click.echo(f"failed to read {f}: {e}", err=True)
            worst_level = max(worst_level, 2)
            continue

        try:
            hb_now = _datetime.fromisoformat(data["now"].replace("Z", "+00:00"))
            freshness = (now_wall - hb_now).total_seconds()
        except (KeyError, ValueError):
            freshness = float("inf")

        ws = data.get("ws_state", "unknown")
        node_level = 0
        if ws == "disconnected" or freshness >= fresh_red_seconds:
            node_level = 2
        elif freshness >= fresh_yellow_seconds:
            node_level = 1

        tier_lags: dict[str, float] = {}
        for tier, ts_iso in (data.get("last_event_ts_per_tier") or {}).items():
            try:
                ts = _datetime.fromisoformat(ts_iso.replace("Z", "+00:00"))
                lag = (now_wall - ts).total_seconds()
                tier_lags[tier] = lag
                if lag >= lag_red_seconds:
                    node_level = max(node_level, 2)
                elif lag >= lag_yellow_seconds:
                    node_level = max(node_level, 1)
            except (ValueError, AttributeError):
                continue

        worst_level = max(worst_level, node_level)
        nodes.append({
            "node_id": node_id,
            "freshness_seconds": round(freshness, 1) if freshness != float("inf") else None,
            "ws_state": ws,
            "tier_lags": {k: round(v, 1) for k, v in tier_lags.items()},
            "metrics": data.get("metrics") or {},
            "level": node_level,
        })

    if fmt == "json":
        click.echo(_json.dumps({"nodes": nodes, "worst_level": worst_level}, indent=2))
    else:
        click.echo(f"Heartbeat status (root={root_resolved})")
        click.echo(
            f"{'node_id':<14}{'fresh':<10}{'ws_state':<16}"
            f"{'lag_tick':<12}{'lag_ob':<12}{'dup_skip':<10}{'quarantine':<10}"
        )
        for n in nodes:
            color = "" if no_color else _level_color(n["level"])
            reset = "" if no_color else "\033[0m"
            fresh_str = (
                f"{n['freshness_seconds']:.1f}s" if n["freshness_seconds"] is not None else "—"
            )
            lag_t = n["tier_lags"].get("tick")
            lag_o = n["tier_lags"].get("orderbook")
            click.echo(
                f"{color}{n['node_id']:<14}{fresh_str:<10}{n['ws_state']:<16}"
                f"{(f'{lag_t:.1f}s' if lag_t is not None else '—'):<12}"
                f"{(f'{lag_o:.1f}s' if lag_o is not None else '—'):<12}"
                f"{n['metrics'].get('dup_skip_count', 0):<10}"
                f"{n['metrics'].get('quarantine_count', 0):<10}"
                f"{reset}"
            )

    sys.exit(worst_level)


if __name__ == "__main__":  # pragma: no cover
    main()
